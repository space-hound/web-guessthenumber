/*=====================================================================================================*/
// IMPORTS
/*=====================================================================================================*/
import { states } from "./../_utils";

/*=====================================================================================================*/
// SELECTORS
/*=====================================================================================================*/
export const selectors = {
    input: "input[type=text]",
    label: "label"
}

/*=====================================================================================================*/
// EVENTS
/*=====================================================================================================*/
export const events = {
    onFocus( event ) {
        this.element.classList.add(states.focus);
        this.element.classList.remove(states.value);
    },

    onFocusOut( event ) {
        this.element.classList.remove(states.focus);
        if(this.__isEmpty()) {
            this.element.classList.add(states.value);
        }
    }
}

/*=====================================================================================================*/
// UTILS
/*=====================================================================================================*/
export const getComponent = ( element, type ) => {
    return element.querySelector(selectors[type]);
}